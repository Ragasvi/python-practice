#Range function
#range func retuns the sequence of numbers starting from 0 by default and increments
#by one by default
x = range(7)
for n in x:
    print(n)

x = range(4,12)
for n in x:
    print(n)
#create a sequence of numbers but increment by 3

x = range(1,30,3)
for n in x:
    print(n)
